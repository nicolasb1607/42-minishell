#include "utils.h"

void	print_tab(char **tab)
{
	int	i;

	i = -1;
	if (tab)
	{
		if (tab[i + 1])
		{
			while (tab && tab[++i])
				ft_putendl_fd(tab[i], 1);
		}
	}
}
